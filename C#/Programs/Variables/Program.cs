namespace Variables
{
    internal class Program
    {
        static void Main(string[] args)
        {
            string name = "Suraj";
            int age = 30;
            
            Console.WriteLine("Hello " + name + "!");
            Console.WriteLine("You are of " + age + " years.");

            //modify variables
            name = "Ashish";
            Console.WriteLine("Hello " + name + "!");
        }
    }
}
/*
 * 1. Variables are containers for storing data values. 
 * 
 * 2. Values can be string, numbers, etc.
