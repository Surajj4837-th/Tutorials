#include <iostream>
using namespace std;

int main()
{
    /*--------------------------------------------------
     1. Fixed size array – manual initialization
    --------------------------------------------------*/
    int a1[5] = {1, 2, 3, 4, 5};

    /*--------------------------------------------------
     2. Fixed size array – partial initialization
        Remaining elements become 0
    --------------------------------------------------*/
    int a2[5] = {10, 20};

    /*--------------------------------------------------
     3. Fixed size array – zero initialization
    --------------------------------------------------*/
    int a3[5] = {0};

    /*--------------------------------------------------
     4. Fixed size array – without initialization
        (Contains garbage values)
    --------------------------------------------------*/
    int a4[5];

    /*--------------------------------------------------
     5. Automatic size deduction
    --------------------------------------------------*/
    int a5[] = {7, 14, 21, 28};

    /*--------------------------------------------------
     6. Character array – string literal
    --------------------------------------------------*/
    char c1[] = "Hello";   // '\0' added automatically

    /*--------------------------------------------------
     7. Character array – explicit characters
    --------------------------------------------------*/
    char c2[6] = {'W', 'o', 'r', 'l', 'd', '\0'};

    /*--------------------------------------------------
     8. 2D array – full initialization
    --------------------------------------------------*/
    int m1[2][3] = {
        {1, 2, 3},
        {4, 5, 6}
    };

    /*--------------------------------------------------
     9. 2D array – partial initialization
    --------------------------------------------------*/
    int m2[2][3] = {
        {1, 2},
        {3}
    };

    /*--------------------------------------------------
     10. 2D array – size deduction for rows
    --------------------------------------------------*/
    int m3[][2] = {
        {1, 2},
        {3, 4},
        {5, 6}
    };

    /*--------------------------------------------------
     11. Multi-dimensional array (3D)
    --------------------------------------------------*/
    int t1[2][2][2] = {
        {{1, 2}, {3, 4}},
        {{5, 6}, {7, 8}}
    };

    /*--------------------------------------------------
     12. Static array – auto zero initialized
    --------------------------------------------------*/
    static int s1[5];

    /*--------------------------------------------------
     13. Const array
    --------------------------------------------------*/
    const int ca[3] = {10, 20, 30};

    /*--------------------------------------------------
     14. Using sizeof to determine array size
    --------------------------------------------------*/
    int a6[] = {5, 10, 15, 20};
    int size = sizeof(a6) / sizeof(a6[0]);

    /*--------------------------------------------------
     15. Dynamic array (heap allocation)
    --------------------------------------------------*/
    int* d1 = new int[5]{1, 2, 3, 4, 5};

    /*--------------------------------------------------
     16. Dynamic array – zero initialization
    --------------------------------------------------*/
    int* d2 = new int[5]();

    /*--------------------------------------------------
     17. Brace-less initialization (C++11)
    --------------------------------------------------*/
    int a7[5]{1, 2, 3};

    /*--------------------------------------------------
     Cleanup dynamic memory
    --------------------------------------------------*/
    delete[] d1;
    delete[] d2;

    return 0;
}
/****************************************************************************************
| Type         | Example                 |
| ------------ | ----------------------- |
| Fixed size   | int a[5] = {1,2,3,4,5}; |
| Partial      | int a[5] = {1,2};       |
| Zero init    | int a[5] = {0};         |
| Auto size    | int a[] = {1,2,3};      |
| Char array   | char c[] = "Hello";     |
| 2D array     | int a[2][3] = {...};    |
| Static array | static int a[5];        |
| Const array  | const int a[3] = {...}; |
| Dynamic      | int* a = new int[5];    |
| C++11 init   | int a[5]{1,2,3};        |
*****************************************************************************************/